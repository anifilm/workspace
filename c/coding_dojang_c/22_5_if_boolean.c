#include <stdio.h>
#include <stdbool.h>        // bool, true, false가 정의된 헤더 파일

int main() {

    if (true)               // boolean -> true
        printf("true\n");
    else
        printf("false\n");

    if (false)              // boolean -> false
        printf("true\n");
    else
        printf("false\n");

    return 0;
}
