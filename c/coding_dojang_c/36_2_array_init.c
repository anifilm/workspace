#include <stdio.h>

int main() {

    int numArr[10] = {0,};   // 배열을 생성하고 값을 0으로 초기화

    printf("numArr[0]: %d\n", numArr[0]);       // 배열의  1번째(인덱스 0) 요소 출력
    printf("numArr[5]: %d\n", numArr[5]);       // 배열의  6번째(인덱스 5) 요소 출력
    printf("numArr[9]: %d\n", numArr[9]);       // 배열의 10번째(인덱스 9) 요소 출력

    return 0;
}
