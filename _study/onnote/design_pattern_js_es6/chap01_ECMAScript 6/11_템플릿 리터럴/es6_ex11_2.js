// 11.2 보간 표현식

// 11.2.1 변수

let firstNumber = 3;
let secondNumber = 1;

let str = `${firstNumber} * ${secondNumber} = 3`;

console.log(str); // 3 * 1 = 3


// 11.2.2 표현식

let firstNumber = 3;
let secondNumber = 1;

let str = `${firstNumber} * ${secondNumber} = ${firstNumber + secondNumber}`;

console.log(str); // 3 * 1 = 3
