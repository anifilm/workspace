// 실수 반올림하기 (round)
#include <iostream>
#include <cmath>

using namespace std;

int main() {

    cout << "=== 실수 반올림 하기 ===" << endl;
    cout << "round(1.1): " << round(1.1) << endl;
    cout << "round(2.3): " << round(2.3) << endl;
    cout << "round(-2.3): " << round(-2.3) << endl;
    cout << "round(40.5): " << round(40.5) << endl;
    cout << "round(55.7): " << round(55.7) << endl;
    cout << "round(-55.7): " << round(-55.7) << endl;
    cout << "round(100.9): " << round(100.9) << endl;

    return 0;
}
