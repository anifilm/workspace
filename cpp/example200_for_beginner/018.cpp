// 정수형 변수 이해하기 (int)
#include <iostream>

using namespace std;

int main() {

    int positive = 100;
    int negative = -200;
    int ascii_value = 'A';

    cout << "양수 값: " << positive << endl;
    cout << "음수 값: " << negative << endl;
    cout << "아스키 값: " << ascii_value << endl;

    return 0;
}

/*

int             -2,147,483,648 ~ +2,147,483,647

unsigned int                 0 ~ 4,294,967,295

*/
