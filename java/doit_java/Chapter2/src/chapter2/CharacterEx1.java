package chapter2;

public class CharacterEx1 {
    public static void main(String[] args) {

        char char1 = 'A';
        System.out.println(char1);
        System.out.println((int)char1);

        char char2 = 66;
        System.out.println(char2);

        char char3 = 67;
        System.out.println(char3);
        System.out.println((char)char3);
    }
}
