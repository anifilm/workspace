class Ex02_char {
    public static void main(String[] args) {
        char ch1 = 'A';
        char ch2 = 65;
        char ch3 = 0x41;
        char ch4 = 0b0000_0000_0100_0001;

        System.out.println(ch1);
        System.out.println(ch2);
        System.out.println(ch3);
        System.out.println(ch4);
    }
}
