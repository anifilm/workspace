// 대입 연산자와 산술 연산자: =, +, -, *, /, %
class ArithOp {
    public static void main(String[] args) {
        int num1 = 7;
        int num2 = 3;

        System.out.println("num1 + num2 = " + (num1 + num2));
        System.out.println("num1 - num2 = " + (num1 - num2));
        System.out.println("num1 * num2 = " + (num1 * num2));
        System.out.println("num1 / num2 = " + (num1 / num2));
        System.out.println("num1 % num2 = " + (num1 % num2));
    }
}
