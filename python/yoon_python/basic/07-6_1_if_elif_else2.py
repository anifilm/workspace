""" 07-6 True 또는 False를 반환하는 연산들 """

def main():
    num = int(input("정수 입력: "))
    if num == 1:  # num이 1이면 True
        print("1을 입력했습니다.")
    elif num == 2:  # num이 2이면 True
        print("2를 입력했습니다.")
    elif num == 3:  # num이 3이면 True
        print("3을 입력했습니다.")
    else:
        print("1, 2, 3 아닌 정수를 입력했습니다.")

main()  # 위의 main 함수를 실행하여라!
