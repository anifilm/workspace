# %-format
# "%datatype" % (variable) 형태로 출력 양식을 표현
print("I eat %d apples." % 3)
print("I eat %d apples." % "five")
number = 3; day = "three"
print("I ate %d apples. I was sick for %s days." % (number, day))
print("Product: %s, Price per unit: %f." % ("Apple", 5.243))

