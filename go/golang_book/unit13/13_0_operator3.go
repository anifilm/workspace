// 연산자 알아보기 3
package main

func main() {
    // + 덧셈
    // 두 값을 더한다. 사용할 수 있는 자료형은 정수, 실수, 복소수, 문자열
    a := 1 + 2                // 3: 두 정수 더하기
    b := 2 + 3                // 5: 두 정수 더하기
    c := a + b                // 8: 두 변수의 값 더하기
    d := "Hello, " + "world!" // Hello, world!: 두 문자열 붙이기
}
