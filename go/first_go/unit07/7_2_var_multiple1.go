// 변수 여러 개 선언하기 1
package main

func main() {
    // var 변수1, 변수2 자료형 = 초기값1, 초기값2
    // var 변수1, 변수2 = 초기값1, 초기값2
    var x, y int = 30, 50       // x = 30, y = 50
    var age, name = 10, "Maria" // age = 10, name = "Maria"

    // 변수1, 변수2 := 초기값1, 초기값2
    a, b, c, d := 1, 3.4, "Hello, world!", false // a = 1, b = 3.4, c = "Hello, world!", d = false
}
