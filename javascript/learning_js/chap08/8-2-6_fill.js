// 8.2.6 특정 값으로 배열 채우기

const arr = new Array(5).fill(1); // [1, 1, 1, 1, 1] 로 초기화
arr.fill('a');       // ["a", "a", "a", "a", "a"]
arr.fill('b', 1);    // ["a", "b", "b", "b", "b"]
arr.fill('c', 2, 4); // ["a", "b", "c", "c", "b"]
arr.fill('c', 2, 4); // ["a", "b", "c", "c", "b"]
arr.fill(5.5, -4);   // ["a", 5.5, 5.5, 5.5, 5.5]
arr.fill(0, -3, -1); // ["a", 5.5, 0, 0, 5.5]
