using System;

// 변환 연산자: () 기호로 데이터 형식 변환하기
int number = (int)3.14;    // 실수 3.14를 정수(int)로 변환하여 저장
Console.WriteLine(number); // 3

long number2 = (long)3.141592; // 실수 3.141592를 정수(long)로 변환하여 저장
Console.WriteLine(number2);    // 3

double number3 = (double)3.141592; // 실수 3.141592를 실수(double)로 변환하여 저장
Console.WriteLine(number3);        // 3.141592
