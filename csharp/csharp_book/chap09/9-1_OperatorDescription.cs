using System;

// 연산자와 피연산자
int num = 1000;
int number = num + 1234;  // + 기호는 연산자, num과 1234는 피연산자
Console.WriteLine(number);

// 연산자(Operator): 더하기, 빼기와 같은 연산을 진행하는 키워드
Console.WriteLine(3 + 5); //  8
Console.WriteLine(3 - 5); // -2
Console.WriteLine(3 * 5); // 15
Console.WriteLine(3 / 5); //  0

// 식(Expression)
// 3 + 5
// 3 - 5

// 문(Statement)
// x = 3 * 5;
// y = 3 / 5;
