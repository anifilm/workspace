using System;

// 기본 자료형을 문자열로 변환
Console.WriteLine((52).ToString() + "\t" + (52).ToString().GetType());
Console.WriteLine((52.273).ToString() + "\t" + (52.273).ToString().GetType());
Console.WriteLine(('a').ToString() + "\t" + ('a').ToString().GetType());
Console.WriteLine((true).ToString() + "\t" + (true).ToString().GetType());
Console.WriteLine((false).ToString() + "\t" + (false).ToString().GetType());

