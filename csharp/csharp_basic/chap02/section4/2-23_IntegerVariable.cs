using System;

class IntegerVariable {
    static void Main() {
        // 정수 변수 생성
        int a = 273;
        int b = 52;

        Console.WriteLine(a + b);
        Console.WriteLine(a - b);
        Console.WriteLine(a * b);
        Console.WriteLine(a / b);
        Console.WriteLine(a % b);
    }
}
